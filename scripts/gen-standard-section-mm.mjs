const entries = {};
for (const line of CSV.trim().split("\n")) {
  const [id, h, b, tw, tf] = line.split(",");
  if (!id) continue;
  entries[id] = { h: +h, b: +b, tw: +tw, tf: +tf };
}

const out = `/* eslint-disable max-len -- generated nominal section sizes */
import type { SectionDimensionsMm, StandardSizeOption } from "./types";

/** EN-style nominal dimensions (mm) keyed by \`StandardSizeOption.id\`. Generated by scripts/gen-standard-section-mm.mjs */
export const STANDARD_SECTION_MM_BY_ID: Record<string, SectionDimensionsMm> = {
${Object.entries(entries)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([id, mm]) => `  ${JSON.stringify(id)}: { h: ${mm.h}, b: ${mm.b}, tw: ${mm.tw}, tf: ${mm.tf} },`)
  .join("\n")}
};

export function withSectionMm(size: StandardSizeOption): StandardSizeOption {
  const mm = STANDARD_SECTION_MM_BY_ID[size.id];
  return mm ? { ...size, sectionMm: mm } : size;
}

export function mapSizes(sizes: StandardSizeOption[]): StandardSizeOption[] {
  return sizes.map(withSectionMm);
}
`;

